// Helper functions
export const formatArticleDate = (dateString) => {
  try {
    // Handle different date formats
    if (dateString.includes(",")) {
      // Format: "March 25, 2025" or "January 15, 2023"
      return new Date(dateString).toLocaleDateString("id-ID", {
        year: "numeric",
        month: "long",
        day: "numeric",
      })
    } else if (dateString.includes(" ")) {
      // Format: "August 2020" or "09 June 2025"
      const parts = dateString.split(" ")
      if (parts.length === 2) {
        // "August 2020"
        return `${parts[0]} ${parts[1]}`
      } else if (parts.length === 3) {
        // "09 June 2025"
        return new Date(`${parts[1]} ${parts[0]}, ${parts[2]}`).toLocaleDateString("id-ID", {
          year: "numeric",
          month: "long",
          day: "numeric",
        })
      }
    }
    return dateString
  } catch (error) {
    return dateString
  }
}

export const getUniqueCategories = () => {
  const categories = articlesData.map((article) => article.kategori)
  return [...new Set(categories)]
}

export const searchArticles = (articles, searchTerm) => {
  if (!searchTerm) return articles

  const term = searchTerm.toLowerCase()
  return articles.filter(
    (article) =>
      article.title.toLowerCase().includes(term) ||
      article.deskripsi.toLowerCase().includes(term) ||
      article.kategori.toLowerCase().includes(term),
  )
}

export const filterArticlesByCategory = (articles, categories) => {
  if (!categories || categories.length === 0) return articles
  return articles.filter((article) => categories.includes(article.kategori))
}

export const sortArticles = (articles, sortOption) => {
  const sortedArticles = [...articles]

  switch (sortOption) {
    case "newest":
      return sortedArticles.sort((a, b) => {
        const dateA = new Date(a.date)
        const dateB = new Date(b.date)
        return dateB - dateA
      })
    case "oldest":
      return sortedArticles.sort((a, b) => {
        const dateA = new Date(a.date)
        const dateB = new Date(b.date)
        return dateA - dateB
      })
    case "a-z":
      return sortedArticles.sort((a, b) => a.title.localeCompare(b.title))
    case "z-a":
      return sortedArticles.sort((a, b) => b.title.localeCompare(a.title))
    default:
      return sortedArticles
  }
}

export const truncateText = (text, maxLength = 150) => {
  if (text.length <= maxLength) return text
  return text.substring(0, maxLength) + "..."
}
