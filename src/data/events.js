// Helper functions
export const formatEventDate = (dateString) => {
  const date = new Date(dateString)
  const months = ["JAN", "FEB", "MAR", "APR", "MAY", "JUN", "JUL", "AUG", "SEP", "OCT", "NOV", "DEC"]

  return {
    date: date.getDate().toString(),
    month: months[date.getMonth()],
    year: date.getFullYear().toString(),
    fullDate: dateString,
  }
}

export const formatEventDateDisplay = (dateString) => {
  const date = new Date(dateString)
  const months = ["JAN", "FEB", "MAR", "APR", "MAY", "JUN", "JUL", "AUG", "SEP", "OCT", "NOV", "DEC"]

  return `${months[date.getMonth()]} ${date.getDate()}, ${date.getFullYear()}`
}

export const getEventStatus = (dateString, endDateString = null) => {
  const today = new Date()
  const eventDate = new Date(dateString)
  const endDate = endDateString ? new Date(endDateString) : eventDate

  today.setHours(0, 0, 0, 0)
  eventDate.setHours(0, 0, 0, 0)
  endDate.setHours(23, 59, 59, 999)

  if (today >= eventDate && today <= endDate) {
    return "ongoing"
  } else if (today > endDate) {
    return "completed"
  } else {
    return "upcoming"
  }
}

export const searchEvents = (events, searchTerm) => {
  if (!searchTerm) return events

  const term = searchTerm.toLowerCase()
  return events.filter(
    (event) =>
      event.title.toLowerCase().includes(term) ||
      event.short_description.toLowerCase().includes(term) ||
      event.location.toLowerCase().includes(term) ||
      event.details.organizer.toLowerCase().includes(term),
  )
}

export const filterEventsByStatus = (events, statuses) => {
  if (!statuses || statuses.length === 0) return events

  return events.filter((event) => {
    const status = getEventStatus(event.date, event.end_date)
    return statuses.includes(status)
  })
}

export const sortEvents = (events, sortOption) => {
  const sortedEvents = [...events]

  switch (sortOption) {
    case "date-asc":
      return sortedEvents.sort((a, b) => new Date(a.date) - new Date(b.date))
    case "date-desc":
      return sortedEvents.sort((a, b) => new Date(b.date) - new Date(a.date))
    case "a-z":
      return sortedEvents.sort((a, b) => a.title.localeCompare(b.title))
    case "z-a":
      return sortedEvents.sort((a, b) => b.title.localeCompare(a.title))
    case "status":
      const statusOrder = { upcoming: 1, ongoing: 2, completed: 3 }
      return sortedEvents.sort((a, b) => {
        const statusA = getEventStatus(a.date, a.end_date)
        const statusB = getEventStatus(b.date, b.end_date)
        return statusOrder[statusA] - statusOrder[statusB]
      })
    default:
      return sortedEvents
  }
}

export const getEventById = (eventId) => {
  return eventsData.events.find((event) => event.id === eventId)
}
